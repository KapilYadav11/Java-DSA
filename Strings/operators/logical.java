import java.util.*;
 public class logical{
    public static void main(String args []){
         System.out.println((3>2) && (5>1));
         System.out.println((3>2) && (5>6));
         // logical AND operator


         System.out.println((5>10) || (5>15));
         System.out.println((5>1) || (5>10));
         // logical OR operator 

         System.out.println(!(3>2));
         // logical NOT 
    }
 }